package com.ict.fly.domain.input;

public class MoreOrderInput {
    private Long renshu;
    private String plane_name;
    private String username;


    public Long getRenshu() {
        return renshu;
    }

    public void setRenshu(Long renshu) {
        this.renshu = renshu;
    }

    public String getPlane_name() {
        return plane_name;
    }

    public void setPlane_name(String plane_name) {
        this.plane_name = plane_name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
