package com.ict.fly.domain.input;

public class PlaneEntity {
    String plane_type;
    int plane_seat;
    String plane_name;
    int plane_lx;
    int plane_ly;
    int plane_use;

    public String getPlane_type() {
        return plane_type;
    }

    public void setPlane_type(String plane_type) {
        this.plane_type = plane_type;
    }

    public int getPlane_seat() {
        return plane_seat;
    }

    public void setPlane_seat(int plane_seat) {
        this.plane_seat = plane_seat;
    }

    public String getPlane_name() {
        return plane_name;
    }

    public void setPlane_name(String plane_name) {
        this.plane_name = plane_name;
    }

    public int getPlane_lx() {
        return plane_lx;
    }

    public void setPlane_lx(int plane_lx) {
        this.plane_lx = plane_lx;
    }

    public int getPlane_ly() {
        return plane_ly;
    }

    public void setPlane_ly(int plane_ly) {
        this.plane_ly = plane_ly;
    }

    public int getPlane_use() {
        return plane_use;
    }

    public void setPlane_use(int plane_use) {
        this.plane_use = plane_use;
    }
}
